class ResearchService:
    """AI 股票研报服务类，提供 Mock 研究报告生成"""

    def __init__(self) -> None:
        """初始化研报服务"""
        self._templates: dict[str, dict] = REPORT_TEMPLATES.copy()
        self._default: dict = DEFAULT_TEMPLATE.copy()

    async def analyze_stock(self, code: str, name: Optional[str] = None) -> ResearchReport:
        """分析股票并生成研究报告

        模拟 AI 研究工作流：
        1. 识别股票
        2. 获取行情
        3. 技术分析
        4. 基本面分析
        5. 风险分析
        6. 生成报告

        Args:
            code: 股票代码
            name: 股票名称（可选）

        Returns:
            生成的研究报告
        """
        # 初始化工作流步骤
        steps: list[ResearchStep] = [
            ResearchStep(id="1", label="识别股票", status="pending"),
            ResearchStep(id="2", label="获取行情", status="pending"),
            ResearchStep(id="3", label="技术分析", status="pending"),
            ResearchStep(id="4", label="基本面分析", status="pending"),
            ResearchStep(id="5", label="风险分析", status="pending"),
            ResearchStep(id="6", label="生成报告", status="pending"),
        ]

        # 模拟工作流执行
        for i, step in enumerate(steps):
            steps[i] = ResearchStep(id=step.id, label=step.label, status="running")
            await asyncio.sleep(0.1)  # 模拟处理延迟
            steps[i] = ResearchStep(id=step.id, label=step.label, status="done")

        # 生成报告
        report = self._generate_mock_report(code, name)
        report.workflow_steps = steps
        return report

    def _generate_mock_report(self, code: str, name: Optional[str] = None) -> ResearchReport:
        """生成 Mock 研究报告

        Args:
            code: 股票代码
            name: 股票名称（可选）

        Returns:
            Mock 研究报告
        """
        template = self._templates.get(code, self._default)
        stock_name = name or self._get_stock_name(code)

        # 如果是默认模板，替换行业占位符
        overview = template["overview"]
        if "{industry}" in overview:
            industry = self._get_stock_industry(code)
            overview = overview.format(industry=industry)

        return ResearchReport(
            stock_code=code,
            stock_name=stock_name,
            overview=overview,
            core_view=template["core_view"],
            technical_analysis=template["technical"],
            fundamental_analysis=template["fundamental"],
            risk_analysis=template["risk"],
            valuation_analysis=template["valuation"],
            investment_advice=template["advice"],
            composite_score=template["score"],
            key_points=template["key_points"],
            workflow_steps=[],  # 由 analyze_stock 填充
            generated_at=datetime.now().strftime("%Y-%m-%d %H:%M:%S"),
        )

